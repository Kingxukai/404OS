#ifndef _TYPE_H__
#define _TYPE_H__

typedef unsigned char uint8_t;			//data 8bit
typedef unsigned short uint16_t;		//data 16bit
typedef unsigned int uint32_t;			//data 32bit
typedef unsigned long uint64_t;		//data 64bit

typedef signed char int8_t;			//data 8bit
typedef signed short int16_t;		//data 16bit
typedef signed int int32_t;			//data 32bit
typedef signed long int64_t;		//data 64bit

typedef uint8_t reg8; 							//register 8bit
typedef uint16_t reg16;					//register 16bit
typedef uint32_t reg32;							//register 32bit
typedef uint64_t reg64;						 //register 64bit

#endif
